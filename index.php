								   <li><b>A4:-</b> The A4 licensed AME has to issue certificates for release to service after minor scheduled line maintenance and simple defect rectification within the limits of maintenance tasks for Helicopter with piston engine.</li>
								   <li><b>B1.1:-</b> Category B1.1 license holder to issue the certificate for the release to service of aeroplane with jet engine.</li>
								   <li><b>B1.4:-</b> Category B1.4 license holder to issue the certificate for the release to service of helicopter with piston engine.</li>
