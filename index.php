								<h4 class="mt-20"><u>AME Course Details</u></h4>
								<p class="justify">The Aircraft Maintenance Engineers are the one important part in Aviation Industry. They are highly
                                 trained to repair and certify the aircraft. All technical work on the Aircraft is carried out under Authority of a Director General of Civil Aviation (DGCA) licensed Aircraft Maintenance Engineer. AME is responsible to certify the aircraft Airworthy condition before each flight. They earn attractive salaries and respect for their professionalism, dedication and hard work.</p>
								<h4 class="mt-20"><u>AME Course Duration</u></h4>
								<p class="justify">For the license A1 and A4, the duration for the course is 1-year academic session. After completion of 1 year in campus, student has to undergo 1-year practical training. The student has to clear the modules and practical training to get the license.</p>
								<p class="justify">For the license B1.1, B1.4, and B2, the duration   for the AME course is 2 years for the academic classes.
                                 After completion of the 2 year’s class room sessions, candidate has to undergo the practical training in
                                  airlines, MRO etc. The students have to clear their modules and practical training to obtain the license.
                                  The licensed AME are authorized to work Internationally.</p>
								 <h4 class="mt-20"><u>AME Course Stream</u></h4>
